package org.projectodd.polyglot.messaging.destinations;

public class AbstractDestinationMetaData {
        
    public AbstractDestinationMetaData() {

    }

    public AbstractDestinationMetaData(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRemoteHost() {
        return remoteHost;
    }

    public void setRemoteHost(String remoteHost) {
        this.remoteHost = remoteHost;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public boolean isRemote() {
        if (remoteHost != null && !remoteHost.isEmpty())
            return true;

        return false;
    }

    public boolean isExported() {
        return exported;
    }

    public void setExported(boolean exported) {
        this.exported = exported;
    }

    /**
     * If the destination should be available via the 'jboss/exported' JNDI context
     * for remote lookups
     */
    private boolean exported = false;
    private String name;
    private String remoteHost;
    private String username;
    private String password;

}
